class VehicleSummary{
  final int vehicleID;
  final String make;
  final String model;
  final String type;
  final double pricePerDay;
  final String vehicleImage;
  final String description;

  VehicleSummary({
    required this.vehicleID,
    required this.make,
    required this.model,
    required this.type,
    required this.pricePerDay,
    required this.vehicleImage,
    required this.description,
});
}